// Funções utilitárias
export const getCategories = () => {
  return [...new Set(imagensGaleria.map(artwork => artwork.category))].sort()
}

export const getTechniques = () => {
  return [...new Set(imagensGaleria.map(artwork => artwork.technique))].sort()
}

export const getYears = () => {
  return [...new Set(imagensGaleria.map(artwork => artwork.year))].sort().reverse()
}
